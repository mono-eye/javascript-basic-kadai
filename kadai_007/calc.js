const num = 30;

// if文を用いて、numが3の倍数の時に「3の倍数です」、そうでないときは数字を出力してください
if (num % 3 === 0) {
  console.log("3の倍数です");
 } if (num % 5 === 0) {
  console.log("5の倍数です");
 } if (num % 15 === 0) {
  console.log('3と5の倍数です');
 } else {
  console.log(num);
}




  